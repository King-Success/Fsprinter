                                <h2>Earthing System</h2>
                            </div>
                            <p>
The earthing system is designed to achieve the uniform earth resistance of less than 1ohm at the site.The grounding system consists of the following Sub Systems:Earth pits, Earth conductor runs from the lightning protection system, and Principal ground bar maintained as reference in the shelter for electronic equipment, DG neutral and DG body to the respective earth pits.
